#include<bits/stdc++.h>
#include<algorithm>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int main()
{
    double v,d;
    double h,ar;

    while(scanf("%lf",&v)!=EOF)
    {
        scanf("%lf",&d);

        h = (4*v)/(3.14*d*d);
        ar=3.14*d*d/4;

        printf("ALTURA = %.2lf\n",h);
        printf("AREA = %.2lf\n",ar);



    }





}
